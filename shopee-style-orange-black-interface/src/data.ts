export function formatPrice(price: number): string {
  return new Intl.NumberFormat("vi-VN").format(price) + "đ";
}

export function formatSold(sold: number): string {
  if (sold >= 1000) {
    return (sold / 1000).toFixed(1) + "k";
  }
  return sold.toString();
}
